/*
 * Define the colors that are indicated by a bit-pair when bit 7 of a
 * data byte is low.
 */
static vm_color colors_h0[] = {
    { 0x00, 0x00, 0x00, 0x00 },     // black
    { 0xff, 0x00, 0xff, 0x00 },     // purple
    { 0x00, 0xff, 0x00, 0x00 },     // green
    { 0xff, 0xff, 0xff, 0x00 },     // white
};

/*
 * Define the colors indicated by a bit-pair when bit 7 of a data byte
 * is high.
 */
static vm_color colors_h1[] = {
    { 0x00, 0x00, 0x00, 0x00 },     // black
    { 0x00, 0x00, 0xff, 0x00 },     // blue
    { 0xff, 0xff, 0x00, 0x00 },     // orange
    { 0xff, 0xff, 0xff, 0x00 },     // white
};

/*
 * These macros define the behavior we use to index colors in a way that
 * can a) be tested in standalone functions, and b) can be used inline
 * elsewhere here.
 */
#define COLOR_H0(b) (colors_h0[(b) & 0x3])
#define COLOR_H1(b) (colors_h1[(b) & 0x3])

int
apple2_hires_row(size_t addr)
{
    return rows[addr - 0x2000];
}

/*
 * Return the column offset of pixels (more than one pixel is in a
 * column-cell) for high-resolution graphics. As it turns out, this is
 * exactly the same as the text column number.
 */
int
apple2_hires_col(size_t addr)
{
    return apple2_text_col(addr);
}

/*
 * Draw the necessary pixels to represent the part of the screen that is
 * governed by the given address. More than one pixel (dot, as the Apple
 * II tech ref calls it) is controlled by a single byte.
 */
void
apple2_hires_draw(apple2 *mach, size_t addr)
{
    int row = apple2_hires_row(addr),
        col = apple2_hires_col(addr);

    row = col;
}

/*
 * Return the color for a given bit-pair when bit 7 of a data byte is
 * low. This function (and the color_h1 one as well) exist primarily to
 * expose the COLOR_Hn macros to testing.
 */
vm_color
apple2_hires_color_h0(vm_8bit byte)
{
    return COLOR_H0(byte);
}

/*
 * Return the color for a given bit-pair when bit 7 of a data byte is
 * high
 */
vm_color
apple2_hires_color_h1(vm_8bit byte)
{
    return COLOR_H1(byte);
}
